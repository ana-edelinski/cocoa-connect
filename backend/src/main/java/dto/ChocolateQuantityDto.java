package dto;

public class ChocolateQuantityDto {
	private int id;
	private int quantity;

	public ChocolateQuantityDto() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

}
